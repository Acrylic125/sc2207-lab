-- CLIENT
CREATE TABLE CLIENT (
    ClientID INT NOT NULL,
    ServiceTier VARCHAR(50) NOT NULL,
    CompanyName VARCHAR(100) NOT NULL,
    StartDate DATE NOT NULL,
    ContactPerson VARCHAR(100),
    CONSTRAINT PK_CLIENT PRIMARY KEY (ClientID)
);
-- CLIENT_HAS_PURCHASEORDER
