    OrderID INT NOT NULL,
    CONSTRAINT PK_CLIENT_HAS_PO PRIMARY KEY (ClientID, OrderID),
    CONSTRAINT FK_CLIENT_HAS_PO_CLIENT FOREIGN KEY (ClientID) REFERENCES CLIENT(ClientID)
